parks = list(filter(lambda x: x != "", parks.split("\n")))

parks = list(filter(lambda x: x.split(" ")[-1] == "Reserve", parks))

import json
from geopy.geocoders import Nominatim
geolocator = Nominatim(user_agent="KenyaResearch")

parks_data = []
for i, park in enumerate(parks):
    print("park ", i, "/", len(parks))
    try:
        location = geolocator.geocode(park)
        parks_data.append({
            "name": park,
            "lat": location.latitude, 
            "long": location.longitude
        })
    except:
        print("Could not find ", park)

print(parks_data)
# print for copying to file
with open("output.txt", "a") as f:
    for park in parks_data:
        print(park, ",", file=f)
    